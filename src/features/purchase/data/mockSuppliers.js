/**
 * Generate a new unique supplier ID
 */
export const generateSupplierId = () => {
  const maxId = MOCK_SUPPLIERS.reduce((max, supplier) => {
    const num = parseInt(supplier.code.split('-')[1]);
    return num > max ? num : max;
  }, 0);
  const newNum = (maxId + 1).toString().padStart(3, '0');
  return `SUP-${newNum}`;
};
